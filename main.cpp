void gotoxy(int xpos, int ypos){
  COORD scrn;
  HANDLE hOuput = GetStdHandle(STD_OUTPUT_HANDLE);
  scrn.X = xpos; scrn.Y = ypos;
  SetConsoleCursorPosition(hOuput, scrn);
}

void SetColor(int f = 7,int b = 0){
    unsigned short ForeColor = f + 16 * b;
    HANDLE hCon = GetStdHandle(STD_OUTPUT_HANDLE);
    SetConsoleTextAttribute(hCon, ForeColor);
}

void print_a_word(int *num, int cnt, int _j, int _k, int x, int y, string indent = ""){
    for (int i = 0; i < cnt; i++){
        gotoxy(x, y);
        for (int j = 0; j < _j; j++){
            for (int k = 0; k < _k; k++){
                int fKind = *(num + i*_j*_k + j*_k + k);
                cout << fireworks[fKind];
            }
            cout << endl << indent;
        }
        Sleep(8);
    }
}

void print_fireworks(int *pic, int frameCount, int _j, int _k, int x, int y, int area = 0){
    int cl = rand() % 13;
    for (int i = 0; i < frameCount; i++){ // frameCount pieces of pictures
        gotoxy(x, y);
        for (int j = area; j < _j - area; j++){ // row
            for (int k = area; k < _k - area; k++){ // column
                int fKind = *(pic + i*_j*_k + j*_k + k);
                cout << fireworks[fKind];
            }
            cout << endl;
            for (int l = 0; l < x; l++){
                cout << " ";
            }
        }
        Sleep(4);
        color.pop_back();
        cl %= 13;
        color.push_back(cols[cl++]);
        system(color.c_str());
    }
    system("cls");



};

